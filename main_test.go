func TestConvolution(t *testing.T) {
	image := [][]int{
		{7, 6, 5, 5, 6, 7},
		{6, 4, 3, 3, 4, 6},
		{5, 3, 2, 2, 3, 5},
		{5, 3, 2, 2, 3, 5},
		{6, 4, 3, 3, 4, 6},
		{7, 6, 5, 5, 6, 7},
	}

	kernel := [][]int{
		{0, -1, 0},
		{-1, 5, -1},
		{0, -1, 0},
	}

	res := [][]int{
		{9, 8, 6, 6, 8, 9},
		{8, 2, 1, 1, 2, 8},
		{6, 1, 0, 0, 1, 6},
		{6, 1, 0, 0, 1, 6},
		{8, 2, 1, 1, 2, 8},
		{9, 8, 6, 6, 8, 9},
	}

	calc := imgManip.Convolution(image, kernel)

	if !reflect.DeepEqual(calc, res) {
		t.Fatalf("Expected %v got %v", res, calc)
	}
}

